class Qcachegrind < Formula
  desc "Visualize data generated by Cachegrind and Calltree"
  homepage "https://kcachegrind.github.io/"
  url "https://download.kde.org/stable/release-service/22.04.3/src/kcachegrind-22.04.3.tar.xz"
  sha256 "b6fc16efd83d076d8b0398fc37f924531203237b7fe28e0a2b4c540c41b29389"
  license "GPL-2.0-or-later"

  # We don't match versions like 19.07.80 or 19.07.90 where the patch number
  # is 80+ (beta) or 90+ (RC), as these aren't stable releases.
  livecheck do
    url "https://download.kde.org/stable/release-service/"
    regex(%r{href=.*?v?(\d+\.\d+\.(?:(?![89]\d)\d+)(?:\.\d+)*)/?["' >]}i)
  end

  bottle do
    sha256 cellar: :any,                 arm64_monterey: "96fdc44520bb6a91877a62dd0fb5dd29f8868401588b5fb8a6fb5c1bc6a92f37"
    sha256 cellar: :any,                 arm64_big_sur:  "50cd0d342a744a6d2f10002e7204a777135ede23a60a5644c226b2a7b9320138"
    sha256 cellar: :any,                 monterey:       "b4377b7f9b6b7a4236eeb4e1de40ac7a964962aa268148d301f8b6a0ef4068c6"
    sha256 cellar: :any,                 big_sur:        "fe654843be55761f5d46231dba525a0fb8a7cc2575469d985c54526b267d93d3"
    sha256 cellar: :any,                 catalina:       "b905084f8c52c3e5d262784b8ea44491d617f6ffcb5aadd3278311d9230ecf80"
    sha256 cellar: :any_skip_relocation, x86_64_linux:   "3a219d9838dd4418ccd3da8c18233b359c2be3ab76e10791426459e338992261"
  end

  depends_on "graphviz"
  depends_on "qt@5"

  on_linux do
    depends_on "gcc"
  end

  fails_with gcc: "5"

  def install
    args = ["-config", "release", "-spec"]
    os = OS.mac? ? "macx" : OS.kernel_name.downcase
    compiler = ENV.compiler.to_s.start_with?("gcc") ? "g++" : ENV.compiler
    arch = Hardware::CPU.intel? ? "" : "-#{Hardware::CPU.arch}"
    args << "#{os}-#{compiler}#{arch}"

    system Formula["qt@5"].opt_bin/"qmake", *args
    system "make"

    if OS.mac?
      prefix.install "qcachegrind/qcachegrind.app"
      bin.install_symlink prefix/"qcachegrind.app/Contents/MacOS/qcachegrind"
    else
      bin.install "qcachegrind/qcachegrind"
    end
  end
end
